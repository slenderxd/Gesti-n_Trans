    public void agregarActualizar(boolean agregar) {
        String idPrestamo = null, idBiblio = null;
        int cuentaAlumno = 0, codigoBarras = 0, res = 0;
        Date fechaIni = null, fechaFin = null;
        java.sql.Date fI = null, fF = null;
        boolean valido = true;

        idPrestamo = this.idPrestamoT.getText();
        if (idPrestamo.isEmpty()) {
            JOptionPane.showMessageDialog(null, "El ID del Prestamo es un dato requerido.",
                    "CAMPO VACIO!", 2);
            valido = false;
            this.idPrestamoT.requestFocus();
        }

        try {
            cuentaAlumno = Integer.parseInt(this.cuentaAlumno.getText());
            if (cuentaAlumno < 1) {
                JOptionPane.showMessageDialog(null, "El numero de cuenta del alumno no puede ser menor a 1.",
                        "ERROR DE DATOS", 2);
                valido = false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El valor del campo de No. Cuenta es invalido. Inrese un dato numérico", "ERROR DE DATOS", 2);
            valido = false;
            this.cuentaAlumno.setText("");
            this.cuentaAlumno.requestFocus();
        }

        try {
            codigoBarras = Integer.parseInt(this.codigoBarras.getText());
            if (codigoBarras < 1) {
                JOptionPane.showMessageDialog(null, "El codigo del material no puede ser menor a 1.",
                        "ERROR DE DATOS", 2);
                valido = false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El valor del campo de codigo del material es invalido. Ingrese un dato numérico", "ERROR DE DATOS", 2);
            valido = false;
            this.codigoBarras.setText("");
            this.codigoBarras.requestFocus();
        }

        idBiblio = this.idBibliotecario.getText();
        if (idBiblio.isEmpty()) {
            JOptionPane.showMessageDialog(null, "El ID del Bibliotecario es un dato requerido.",
                    "CAMPO VACIO!", 2);
            valido = false;
            this.idBibliotecario.requestFocus();
        }

        fechaIni = fechaInicio.getDate();
        if (fechaIni == null) {
            JOptionPane.showMessageDialog(null, "La Fecha de Inicio de Prestamo es un dato requerido.",
                    "CAMPO VACIO!", 2);
            valido = false;
        } else {
            fI = new java.sql.Date((long) fechaIni.getTime());
        }

        fechaFin = this.fechaFin.getDate();
        if (fechaFin == null) {
            JOptionPane.showMessageDialog(null, "La Fecha de Fin de Prestamo es un dato requerido.",
                    "CAMPO VACIO!", 2);
            valido = false;
        } else {
            fF = new java.sql.Date((long) fechaFin.getTime());
        }
        
        if(agregar && valido){
            res = PBD.agregarRegistroPrestamo(idPrestamo, cuentaAlumno, codigoBarras, idBiblio, fI, fF);
        } else if(!agregar && valido){
            res= PBD.actualizarRegistroPrestamo(idPrestamo, cuentaAlumno, codigoBarras, idBiblio, fI, fF);
        }
        if(res > 0){
            this.listar();
            this.limpiar();
        }
    }

