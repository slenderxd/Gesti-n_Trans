    private void DTM_PrestamosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DTM_PrestamosMouseClicked
        // TODO add your handling code here:
        this.limpiar();
        int fila = DTM_Prestamos.getSelectedRow();
        idPrestamoT.setEnabled(false);
        
        idPrestamoT.setText(DTM_Prestamos.getValueAt(fila, 0).toString());
        cuentaAlumno.setText(DTM_Prestamos.getValueAt(fila, 1).toString());
        codigoBarras.setText(DTM_Prestamos.getValueAt(fila, 2).toString());
        idBibliotecario.setText(DTM_Prestamos.getValueAt(fila, 3).toString());
        String fechaIni = DTM_Prestamos.getValueAt(fila, 4).toString();
        java.util.Date dateI;
        try {
            dateI = new SimpleDateFormat("yyyy-MM-dd").parse(fechaIni);
            fechaInicio.setDate(dateI);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, "Error al cargar la fecha de inicio de prestamo.", "ERROR", 2);
        }
        String fechaF = DTM_Prestamos.getValueAt(fila, 5).toString();
        java.util.Date dateF;
        try {
            dateF = new SimpleDateFormat("yyyy-MM-dd").parse(fechaF);
            this.fechaFin.setDate(dateF);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, "Error al cargar la fecha de fin de prestamo.", "ERROR", 2);
        }
    }//GEN-LAST:event_DTM_PrestamosMouseClicked

